    class LRUCache {
        class Node {
            int key;
            int value;
            Node prev;
            Node next;

            public Node() {
            }

            public Node(int key, int value) {
                this.key = key;
                this.value = value;
            }
        }

        private Map<Integer, Node> map;
        private int capacity;
        private int size;
        private Node head;
        private Node tail;

        // æ·»åŠ çš„å…ƒç´ éƒ½åœ¨é˜Ÿåˆ—å¤´éƒ¨
        // è®°å½•å½“å‰å…ƒç´ çš„ä¸ªæ•°ï¼Œå½“ä¸ªæ•°è¶…å‡ºç¼“å­˜å¤§å°æ—¶ï¼Œç§»é™¤æœ«å°¾çš„å…ƒç´ 


        public LRUCache(int capacity) {
            this.capacity = capacity;
            map = new HashMap<>();
            size = 0;
            head = new Node();
            tail = new Node();
            head.next = tail;
            tail.prev = head;
        }

        public int get(int key) {
//            printNode();
            if (!map.containsKey(key)) {
                return -1;
            }
            Node node = map.get(key);

            moveToHead(node);
            return node.value;
        }

        public void put(int key, int value) {
            Node node = map.get(key);
            if (node == null) {
                node = new Node(key, value);
                addToHead(node);
                map.put(key, node);
                size++;
                if (size > capacity) {
                    removeTail();
                }
            } else {
                // æ›´æ–°
                node.value = value;
                moveToHead(node);
            }
        }

        private void moveToHead(Node node) {
            remove(node);
            addToHead(node);
        }

        private void remove(Node node) {
            node.prev.next = node.next;
            node.next.prev = node.prev;
        }

        private void removeTail() {
            size--;
            map.remove(tail.prev.key);
            tail.prev.prev.next = tail;
            tail.prev = tail.prev.prev;
        }

        private void addToHead(Node node) {
            node.next = head.next;
            node.prev = head;
            head.next.prev = node;
            head.next = node;
        }

        private void printNode() {
            Node node = head.next;
            while (node != tail) {
                System.out.print("[" + node.key + "," + node.value + "] ");
                node = node.next;
            }
            System.out.println();
        }
    }

